        function addCart(obj){
            var id = $(obj).data('id');
            
            $.ajax({
                url: "<?= base_url('produk/addtocart') ?>",
                type: 'post',
                dataType: 'json',
                data: {
                    id: id
                },
                success: function(result){
                    console.log(result);
                    if(result.sukses == 1){
                        swal('Sukses',result.pesan,'success');
                        setTimeout(function(){
                            window.location.href = '<?= base_url('Home/cart') ?>';
                        },1000);
                    }else{
                        swal('Gagal',result.pesan,'error');
                    }
                }
            });
        }

        function totalcart(){
            var total = 0;
            $('b[id="total_per_produk"]').each(function(index, value){
                var harga = parseInt($(value).html().replace(',',''));
                total += harga;
            });

            $('#total_keseluruhan').html((total/1000).toFixed(3).replace('.',','));
        }

        function ubahjumlahproduk(obj){
            var jumlah = $(obj).val();
            if(jumlah < 1){
                swal({
                    title: "Hapus",
                    text: "Apakah anda yakin hapus produk ini dari keranjang anda?",
                    icon: "warning",
                    buttons: true,
                    dangerMode: true,
                }).then((willDelete) => {
                    if (willDelete) {
                        var cart_id = $(obj).parents('tr').find('#cart_id').val();
                        // alert(cart_id);return false;
                        $.ajax({
                            url: "<?= base_url('produk/hapusdaricart') ?>",
                            type: 'post',
                            dataType: 'json',
                            data: {
                                cart_id: cart_id
                            },
                            success: function(result){
                                // console.log(result);
                                if(result.sukses == 1){
                                    $(obj).parents('tr').detach();
                                    totalcart();
                                    swal('Sukses',result.pesan,'success');
                                }else{
                                    swal('Gagal',result.pesan,'error');
                                    $(obj).val(1);
                                }
                            }
                        });
                    } else {
                        $(obj).val(1);
                    }
                    });
            }else{
                var cart_id = $(obj).parents('tr').find('#cart_id').val();
                // alert(cart_id);return false;
                $.ajax({
                    url: "<?= base_url('produk/ubahjumlahcart') ?>",
                    type: 'post',
                    dataType: 'json',
                    data: {
                        cart_id: cart_id,
                        jumlah: jumlah
                    },
                    success: function(result){
                        // console.log(result);
                        if(result.sukses == 1){
                            var total_baru = result.total_baru;
                            $(obj).parents('tr').find('#total_per_produk').html(total_baru);
                            totalcart();
                        }else{
                            swal('Gagal',result.pesan,'error');
                        }
                    }
                });
            }
        }

